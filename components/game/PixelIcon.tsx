/* ------------------------------------------------------------------ */
/*  Animation class map                                                 */
/* ------------------------------------------------------------------ */

const ANIMATION_MAP: Partial<Record<PixelIconName, string>> = {
  coin: 'animate-pixel-coin',
  star: 'animate-pixel-sparkle',
  heart: 'animate-pixel-heart',
};

/* ------------------------------------------------------------------ */
/*  Component                                                           */
/* ------------------------------------------------------------------ */

export function PixelIcon({ icon, size = 3, animated = false, className }: PixelIconProps) {
  const pixels = ICON_DATA[icon];

  const boxShadow = useMemo(() => {
    return pixels
      .map(([x, y, color]) => `${x * size}px ${y * size}px 0 0 ${color}`)
      .join(', ');
  }, [pixels, size]);

  // Determine bounding box
  const bounds = useMemo(() => {
    let maxX = 0;
    let maxY = 0;
    for (const [x, y] of pixels) {
      if (x > maxX) maxX = x;
      if (y > maxY) maxY = y;
    }
    return { width: (maxX + 1) * size + size, height: (maxY + 1) * size + size };
  }, [pixels, size]);

  const animationClass = animated ? ANIMATION_MAP[icon] : undefined;

  return (
    <div
      className={cn('relative inline-block pixel-art', animationClass, className)}
      style={{ width: bounds.width, height: bounds.height }}
      role="img"
      aria-label={`Icone ${icon}`}
    >
      <div
        style={{
          position: 'absolute',
          top: 0,
          left: 0,
          width: size,
          height: size,
          backgroundColor: 'transparent',
          boxShadow,
        }}
      />
    </div>
  );
}
